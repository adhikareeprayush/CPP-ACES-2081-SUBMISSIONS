/*
  This file demonstrates a simple comparison between C-style and C++-style input/output.
*/

#include <stdio.h>  // C-style
#include <iostream> // C++-style

using namespace std;

int main() {
    // C-style output
    printf("C-style output: Hello, World!\n");

    // C++-style output
    cout << "C++-style output: Hello, World!" << endl;

    return 0;
}
